-- 자연조인을 사용하여 쉽게 구할 수 있음
SELECT DISTINCT A.FLAVOR
FROM FIRST_HALF A, ICECREAM_INFO B
WHERE A.TOTAL_ORDER>3000 AND B.INGREDIENT_TYPE='fruit_based' AND A.FLAVOR = B.FLAVOR
ORDER BY A.TOTAL_ORDER DESC


